function ReadFolderDirectory($dir) 
    { 
        $listDir = array(); 
        if($handler = opendir($dir)) { 
            while (($sub = readdir($handler)) !== FALSE) { 
                if ($sub != "." && $sub != ".." && $sub != "Thumbs.db" && $sub != "Thumb.db") { 

                    if(is_file($dir."/".$sub)) { 

                        $listDir[] = array('label' => ($sub=='0.png'?' Default ':$sub),'src' => get_bloginfo ( 'template_url' )."/images/bgrs/".$sub ,'value' => get_bloginfo ( 'template_url' )."/images/bgrs/".$sub); 
                        
                    }elseif(is_dir($dir."/".$sub)){ 
                        $listDir[] = ReadFolderDirectory($dir."/".$sub); 
                    } 
                } 
            }    
            closedir($handler); 
        } 
        sort($listDir);
        return $listDir;    
    }
    

  

